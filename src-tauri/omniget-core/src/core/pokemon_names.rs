pub fn random_pokemon_name() -> &'static str {
    use std::time::{SystemTime, UNIX_EPOCH};
    let seed = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_nanos() as usize;
    POKEMON_NAMES[seed % POKEMON_NAMES.len()]
}
